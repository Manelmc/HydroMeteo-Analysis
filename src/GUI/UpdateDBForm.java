    private File[] findAlltxtFiles( String dirName){
    	File dir = new File(dirName);

    	return dir.listFiles(new FilenameFilter() { 
                @Override
    	        public boolean accept(File dir, String filename)
    	            { return filename.endsWith(".txt") || filename.endsWith(".csv"); }
    	} );

    }
    class Browse implements ActionListener {
        @Override
        public void actionPerformed(ActionEvent e) {
            JFileChooser c = new JFileChooser();
            c.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);
            int rVal = c.showSaveDialog(UpdateDBForm.this);
            if (rVal == JFileChooser.APPROVE_OPTION) {
              //filename.setText(c.getSelectedFile().getName());

              jTextField1.setText(c.getSelectedFile().getAbsolutePath());
            }
            if (rVal == JFileChooser.CANCEL_OPTION) {
              //filename.setText("You pressed cancel");
              jTextField1.setText("");
            }
        }
    }

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String Path = jTextField1.getText();
        if(!"".equals(Path)){
            if (JOptionPane.showConfirmDialog(this, 
                "Confirm Database Update?", "Database Update", 
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE) == JOptionPane.YES_OPTION){
                    setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
                    if(Path.contains(".txt") || Path.contains(".csv")){
                        int errors = GUIcon.updateDB(Path, table);
                        showMessageDialog(null,  "DataBase Updated!" + "\n" + errors + " Errors");
                    }else if(!Path.contains(".")){
                        File[] files; 
                        files = findAlltxtFiles(Path);
                        int errors = 0;
                        for(int i = 0; i < files.length; ++i){
                             errors += GUIcon.updateDB(files[i].getAbsolutePath(), table);
                        }
                        showMessageDialog(null, "DataBase Updated!" + "\n" + errors + " Errors");
                    }
                    setCursor(Cursor.getDefaultCursor());
                    main.setEnabled(true);
                    dispose();  
                }
            }else{
            showMessageDialog(null, "Please, browse a valid Path");
            }
    }//GEN-LAST:event_jButton2ActionPerformed

